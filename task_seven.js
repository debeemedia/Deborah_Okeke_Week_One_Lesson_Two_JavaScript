// Use the "switch" statement

const day = 'Sunnyday'

switch (day) {
  case 'Sunday':
    console.log('Sunday: First day of the week')
    break
  case 'Monday':
    console.log('Monday: Second day of the week')
    break
  case 'Tuesday':
    console.log('Tuesday: Third day of the week')
    break
  case 'Wednesday':
    console.log('Wednesday: Fourth day of the week')
    break
  case 'Thursday':
    console.log('Thursday: Fifth day of the week')
    break
  case 'Friday':
    console.log('Friday: Sixth day of the week')
    break
  case 'Saturday':
    console.log('Saturday: Last day of the week')
    break
  default:
    console.log('You dey carry us dey go wia we nor know o!')
    break
}
